# Equal to (==)
a = 5
b = 5
print(a == b)  # Output: True

# Not equal to (!=)
a = 5
b = 3
print(a != b)  # Output: True

# Greater than (>)
a = 5
b = 3
print(a > b)  # Output: True

# Less than (<)
a = 3
b = 5
print(a < b)  # Output: True

# Greater than or equal to (>=)
a = 5
b = 5
print(a >= b)  # Output: True

# Less than or equal to (<=)
a = 3
b = 5
print(a <= b)  # Output: True