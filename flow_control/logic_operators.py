# AND
# Example 1
a = True
b = True
print(a and b)  # Output: True

# Example 2
a = True
b = False
print(a and b)  # Output: False

# OR
# Example 1
a = True
b = False
print(a or b)  # Output: True

# Example 2
a = False
b = False
print(a or b)  # Output: False

#  NOT
# Example 1
a = True
print(not a)  # Output: False

# Example 2
a = False
print(not a)  # Output: True

