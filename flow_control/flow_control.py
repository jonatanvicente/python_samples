# Elif
# Example 1
x = 10
if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but less than or equal to 15")
else:
    print("x is 5 or less")

# Example 2
y = 3
if y > 15:
    print("y is greater than 15")
elif y > 5:
    print("y is greater than 5 but less than or equal to 15")
else:
    print("y is 5 or less")
