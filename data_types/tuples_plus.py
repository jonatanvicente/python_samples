# Tuple containing integers
int_tuple = (1, 2, 3)
print(int_tuple)  # Output: (1, 2, 3)

# Tuple containing floats
float_tuple = (1.1, 2.2, 3.3)
print(float_tuple)  # Output: (1.1, 2.2, 3.3)

# Tuple containing strings
string_tuple = ("apple", "banana", "cherry")
print(string_tuple)  # Output: ("apple", "banana", "cherry")

# Tuple containing booleans
bool_tuple = (True, False, True)
print(bool_tuple)  # Output: (True, False, True)

# Tuple containing mixed data types
mixed_tuple = (1, "apple", 3.14, True)
print(mixed_tuple)  # Output: (1, "apple", 3.14, True)

# Tuple containing a list
list_tuple = ([1, 2, 3], [4, 5, 6])
print(list_tuple)  # Output: ([1, 2, 3], [4, 5, 6])

# Tuple containing a dictionary
dict_tuple = ({"name": "John"}, {"age": 30})
print(dict_tuple)  # Output: ({"name": "John"}, {"age": 30})

# Tuple containing a set
set_tuple = ({1, 2, 3}, {4, 5, 6})
print(set_tuple)  # Output: ({1, 2, 3}, {4, 5, 6})

# Tuple containing a tuple
tuple_tuple = ((1, 2), (3, 4))
print(tuple_tuple)  # Output: ((1, 2), (3, 4))

# Tuple containing NoneType
none_tuple = (None, None, None)
print(none_tuple)  # Output: (None, None, None)